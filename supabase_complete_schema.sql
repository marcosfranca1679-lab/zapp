-- ---------------------------------------------------------
-- 1. TABELA: notifications
-- ---------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.notifications (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    title TEXT NOT NULL,
    message TEXT NOT NULL,
    target_user TEXT DEFAULT NULL, -- NULL = global, caso contrário = MAC Address
    is_active BOOLEAN DEFAULT TRUE NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Habilitar RLS
ALTER TABLE public.notifications ENABLE ROW LEVEL SECURITY;

-- Políticas de Acesso
DROP POLICY IF EXISTS "Leitura de notificações para todos" ON public.notifications;
DROP POLICY IF EXISTS "Escrita e controle total de notificações" ON public.notifications;

CREATE POLICY "Leitura de notificações para todos" 
ON public.notifications FOR SELECT USING (true);

CREATE POLICY "Escrita e controle total de notificações" 
ON public.notifications FOR ALL USING (true) WITH CHECK (true);


