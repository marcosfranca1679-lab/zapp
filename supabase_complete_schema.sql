-- ---------------------------------------------------------
-- 5. BUCKET DE IMAGENS: storage.buckets
-- ---------------------------------------------------------
INSERT INTO storage.buckets (id, name, public)
VALUES ('banners', 'banners', true)
ON CONFLICT (id) DO NOTHING;

-- Políticas de Acesso para o Storage
DROP POLICY IF EXISTS "Permitir leitura pública de banners" ON storage.objects;
DROP POLICY IF EXISTS "Permitir upload de banners" ON storage.objects;
DROP POLICY IF EXISTS "Permitir exclusão de banners" ON storage.objects;

CREATE POLICY "Permitir leitura pública de banners"
ON storage.objects FOR SELECT
USING (bucket_id = 'banners');

CREATE POLICY "Permitir upload de banners"
ON storage.objects FOR INSERT
WITH CHECK (bucket_id = 'banners');

CREATE POLICY "Permitir exclusão de banners"
ON storage.objects FOR DELETE
USING (bucket_id = 'banners');
